package net.liujian.cheer.sample.ch03;

public class Compare {

	public static void main(String[] args) {
		int i = 4;
		int j = 5;
		System.out.println("i>j的返回值为:"+(i>j));
		System.out.println("i<j的返回值为:"+(i<j));
		System.out.println("i==j的返回值为:"+(i==j));
		System.out.println("i>=j的返回值为:"+(i>=j));
		System.out.println("i<=j的返回值为:"+(i<=j));
		System.out.println("i!=j的返回值为:"+(i!=j));
	}

}
